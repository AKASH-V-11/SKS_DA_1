import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt

"""# 2ï¸âƒ£ Load Dataset"""

from google.colab import files
uploaded = files.upload()

df = pd.read_excel("Telco_Customer_Churn_Dataset.xlsx")

"""# 3ï¸âƒ£ Initial Data Overview"""

print("\nðŸ” Dataset Shape:", df.shape)
df.head()

"""# 4ï¸âƒ£ Check for Missing Values"""

print("\nðŸ§¼ Missing Values:")
print(df.isnull().sum())

"""# 5ï¸âƒ£ Visualize Missing Data"""

plt.figure(figsize=(10,6))
sns.heatmap(df.isnull(), cbar=False, cmap="YlGnBu")
plt.title("Missing Value Heatmap")
plt.show()

"""# 6ï¸âƒ£ Drop Unnecessary Column"""

# 'customerID' is irrelevant for prediction
df.drop('customerID', axis=1, inplace=True)

"""# 7ï¸âƒ£ Convert 'TotalCharges' to Numeric (fix object dtype)"""

df['TotalCharges'] = pd.to_numeric(df['TotalCharges'], errors='coerce')

# Impute missing TotalCharges with median
df['TotalCharges'].fillna(df['TotalCharges'].median(), inplace=True)

"""# 8ï¸âƒ£ Binary Label Encoding (Yes/No â†’ 1/0)"""

binary_cols = ['Partner', 'Dependents', 'PhoneService', 'PaperlessBilling', 'Churn']
for col in binary_cols:
    df[col] = df[col].map({'Yes': 1, 'No': 0})

"""# 9ï¸âƒ£ One-Hot Encoding for Multi-Class Categorical Columns"""

multi_class_cols = ['MultipleLines', 'InternetService', 'OnlineSecurity', 'OnlineBackup',
                    'DeviceProtection', 'TechSupport', 'StreamingTV', 'StreamingMovies',
                    'Contract', 'PaymentMethod', 'gender']

# Apply One-Hot Encoding
df = pd.get_dummies(df, columns=multi_class_cols, drop_first=True)

"""# ðŸ”Ÿ Final Dataset Check"""

print("\nâœ… Data Types After Encoding:")
print(df.dtypes)
print("\nðŸ“Œ Cleaned Data Preview:")
df.head()

"""# ðŸ”„ Save Cleaned Dataset"""

df.to_csv("cleaned_churn_data.csv", index=False)
print("\nðŸ’¾ Cleaned data saved as 'cleaned_churn_data.csv'")

